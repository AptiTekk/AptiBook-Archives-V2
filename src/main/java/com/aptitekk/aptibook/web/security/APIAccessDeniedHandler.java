package com.aptitekk.aptibook.web.security;

import com.aptitekk.aptibook.web.api.APIResponse;
import org.springframework.security.access.AccessDeniedException;
import org.springframework.security.web.access.AccessDeniedHandler;
import org.springframework.stereotype.Component;

import javax.servlet.ServletException;
import javax.servlet.http.HttpServletRequest;
import javax.servlet.http.HttpServletResponse;
import java.io.IOException;

/**
 * Defines the behavior when access is denied by Spring Security.
 */
@Component
public class APIAccessDeniedHandler implements AccessDeniedHandler {

    @Override
    public void handle(HttpServletRequest request, HttpServletResponse response, AccessDeniedException accessDeniedException) throws IOException, ServletException {
        APIResponse.writeToResponse(APIResponse.unauthorized("unauthorized", accessDeniedException.getMessage()), response);
    }

}
